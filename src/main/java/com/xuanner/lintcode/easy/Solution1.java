package com.xuanner.lintcode.easy;

/**
 * 1. A + B 问题
 * 给出两个整数a和b, 求他们的和, 但不能使用 + 等数学运算符。
 * 样例
 * 如果 a=1 并且 b=2，返回3。
 * 挑战
 * 显然你可以直接 return a + b，但是你是否可以挑战一下不这样做？
 * Created by xuan on 2018/5/14.
 */
public class Solution1 {

    /**
     * @param a: An integer
     * @param b: An integer
     * @return: The sum of a and b
     */
    public int aplusb(int a, int b) {
        return a + b;
    }
}
